void shuffle(int* v, int t)
{
long r1;
long r2;
int a;
for(int i=0;i<(int)(t/2.0);i++)
	{
	do
		{
		r1=((long)(time(0)*rand()/100.0))%t;
		r2=((long)(time(0)*rand()/100.0))%t;
		}while(v[r1]==v[r2]);
	a=v[r1];
	v[r1]=v[r2];
	v[r2]=a;
	}
}
